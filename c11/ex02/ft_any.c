int	ft_any(char **tab, int(*f)(char*))
{
	int	i;
	int	cpt;

	cpt = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]))
		{
			cpt = 1;
			break ;
		}
		i++;
	}
	return (cpt);
}
