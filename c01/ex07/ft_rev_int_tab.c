void	ft_rev_int_tab(int *tab, int size)
{
	int		temp;
	int		i;
	int		n;

	n = size - 1;
	i = 0;
	while (i < n - i)
	{
		temp = tab[i];
		tab[i] = tab[n - i];
		tab[n - i] = temp;
		i++;
	}
}
